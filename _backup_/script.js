// 2. ฟังก์ชันสร้างการ์ดแสดงหน้าเว็บ (ไม่ต้องแก้ไข ยกเว้นอยากเปลี่ยนชื่อ id)
function renderCards() {
    const container = document.getElementById('activity-container'); // ต้องมี <div id="activity-container"> ใน HTML
    if (!container) return;

    container.innerHTML = activities.map((act, index) => `
        <div class="card" onclick="openModal(${index})">
            <img src="${act.images[0]}" alt="${act.title}">
            <div class="card-content">
                <h3>${act.title}</h3>
                <p>${act.short}</p>
                <a href="javascript:void(0);" class="card-btn">อ่านเพิ่มเติม</a>
            </div>
        </div>
    `).join('');
}

// 3. ฟังก์ชันเปิด Modal (ดึงข้อมูลจาก Array ตามลำดับ index มาโชว์)
function openModal(index) {
    const data = activities[index];
    const modal = document.getElementById('mainModal'); // ต้องมี <div id="mainModal"> ใน HTML
    
    // แสดงรูปหลัก (เอารูปแรกจาก Array มาโชว์ก่อน)
    document.getElementById('modal-main-img').src = data.images[0];
    
    // ใส่ชื่อเรื่องและเนื้อหาละเอียด
    document.getElementById('modal-text').innerHTML = `
        <h2 style="color: #8a0000; margin-top: 20px;">${data.title}</h2>
        <p>${data.desc}</p>
    `;
    
    // สร้างปุ่มรูปเล็กๆ (Thumbnails) ตามจำนวนรูปที่มีใน Array
    const thumbContainer = document.getElementById('modal-thumbnails');
    thumbContainer.innerHTML = data.images.map((img, i) => 
        `<img src="${img}" class="thumb${i === 0 ? ' active' : ''}" onclick="switchImage(this)">`
    ).join('');

    modal.style.display = "block";
    document.body.style.overflow = "hidden"; 
}

// ฟังก์ชันเปลี่ยนรูปหลักและ highlight thumbnail ที่เลือก
function switchImage(thumb) {
    document.getElementById('modal-main-img').src = thumb.src;
    document.querySelectorAll('.thumb').forEach(t => t.classList.remove('active'));
    thumb.classList.add('active');
}

// 4. ฟังก์ชันปิด Modal
function closeModal() {
    const modal = document.getElementById('mainModal');
    modal.style.display = "none";
    document.body.style.overflow = "auto"; 
}

// ปิดเมื่อคลิกนอกกรอบ
window.onclick = function(event) {
    const modal = document.getElementById('mainModal');
    if (event.target == modal) {
        closeModal();
    }
}

